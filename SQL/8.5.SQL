IF EXISTS ISSUESLISTS DROP ISSUESLISTS;
IF EXISTS ISSUES DROP ISSUES;


SELECT I.SUMMARY, I.DESCRIPTION, IL.NAME
FROM ISSUES I
JOIN ISSUESLISTS IL ON I.ISSUESLIST_ID = IL.ID;



SELECT I.SUMMARY, I.DESCRIPTION, U.FIRSTNAME, U.LASTNAME
FROM ISSUES I
JOIN USERS U ON I.USERS_ID_ASSIGNEDTO = U.ID;



SELECT  U.FIRSTNAME, U.LASTNAME, COUNT(*) AS ISSUES_QUANTITY
FROM USERS U
JOIN ISSUES I ON U.ID = I.USERS_ID_ASSIGNEDTO
GROUP BY U.ID
HAVING COUNT(*) >0;

