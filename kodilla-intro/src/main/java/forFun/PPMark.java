package forFun;

public class PPMark {

    String name;
    String surname;
    int math;
    int pe;
    int geography;
    int biology;

    public PPMark(String name, String surname, int math, int pe, int geography, int biology) {
        this.name = name;
        this.surname = surname;
        this.math = math;
        this.pe = pe;
        this.geography = geography;
        this.biology = biology;
    }
}
